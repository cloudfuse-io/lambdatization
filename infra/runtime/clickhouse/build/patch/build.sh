#!/bin/bash

pushd ClickHouse
mkdir -p build
pushd build
cmake .. \
    -DCMAKE_BUILD_TYPE=Release \
    -DENABLE_CLICKHOUSE_ALL=OFF \
    -DENABLE_CLICKHOUSE_SERVER=ON \
    -DENABLE_CLICKHOUSE_CLIENT=ON \
    -DENABLE_AVX=ON \
    -DENABLE_AVX2=ON \
    -DENABLE_LIBRARIES=OFF \
    -DUSE_UNWIND=ON \
    -DENABLE_UTILS=OFF \
    -DENABLE_TESTS=OFF \
    -DENABLE_EMBEDDED_COMPILER=0 \
    -DENABLE_HDFS=0 \
    -DENABLE_AZURE_BLOB_STORAGE=0 \
    -DENABLE_CASSANDRA=0 \
    -DENABLE_KRB5=0 \
    -DENABLE_ODBC=0 \
    -DENABLE_AMQPCPP=0 \
    -DENABLE_LDAP=0 \
    -DENABLE_SSL=OFF
ninja
popd
popd
